/**
 * Builds the PfIcon “sprite”: reads all `src/icons/svg/*.svg`,
 * generates `src/components/element/PfIcon/paths.ts` (viewBox + paths for inline SVG).
 *
 * Glyph name: `kebab-case.svg` → camelCase (`chevron-down.svg` → `chevronDown`).
 * One-off Figma import: `node scripts/extract-pf-icons.mjs …`.
 *
 * Usage: node scripts/build-pf-icons-from-svg.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const svgDir = path.join(root, 'src/icons/svg');
const outPath = path.join(root, 'src/components/element/PfIcon/paths.ts');

function kebabBaseToCamelCase(base) {
  const parts = base.split(/[-_]+/).filter(Boolean);
  if (parts.length === 0) return '';
  return parts
    .map((p, i) =>
      i === 0
        ? p.toLowerCase()
        : p.charAt(0).toUpperCase() + p.slice(1).toLowerCase()
    )
    .join('');
}

function isValidIconName(name) {
  return /^[a-z][a-zA-Z0-9]*$/.test(name);
}

function normalizeFills(html) {
  return html
    .replace(/\sfill="black"/g, ' fill="currentColor"')
    .replace(/\sfill="#293D4E"/g, ' fill="currentColor"')
    .replace(/\sfill="#002952"/g, ' fill="currentColor"')
    .replace(/\sfill="white"/g, ' fill="var(--pf-color-surface)"');
}

function parseViewBox(svg) {
  const open = svg.match(/<svg\b[^>]*>/is);
  if (!open) return '0 0 24 24';
  const tag = open[0];
  const vb = tag.match(/\bviewBox=["']([^"']+)["']/i);
  if (vb) return vb[1].trim().replace(/\s+/g, ' ');
  const wM = tag.match(/\bwidth=["']?([\d.]+)/i);
  const hM = tag.match(/\bheight=["']?([\d.]+)/i);
  if (wM && hM) {
    const w = Number(wM[1]);
    const h = Number(hM[1]);
    if (!Number.isNaN(w) && !Number.isNaN(h)) return `0 0 ${w} ${h}`;
  }
  return '0 0 24 24';
}

function matchAttr(attrChunk, ...names) {
  for (const name of names) {
    const re = new RegExp(
      `\\b${name.replace(/-/g, '\\-')}=["']([^"']*)["']`,
      'i'
    );
    const m = attrChunk.match(re);
    if (m) return m[1];
  }
  return undefined;
}

/** Inner `<svg>` markup without the outer tag; strips `<defs>` (gradients, etc.). */
function svgInnerWithoutDefs(svg) {
  const inner = svg
    .replace(/^[\s\S]*?<svg\b[^>]*>/i, '')
    .replace(/<\/svg>\s*$/i, '');
  return inner.replace(/<defs[\s\S]*?<\/defs>/gi, '');
}

function extractPaths(inner) {
  const paths = [];
  let i = 0;
  while (i < inner.length) {
    const start = inner.indexOf('<path', i);
    if (start === -1) break;
    let pos = start + 5;
    while (pos < inner.length && /\s/.test(inner[pos])) pos++;

    const selfClose = inner.indexOf('/>', pos);
    const gt = inner.indexOf('>', pos);
    let attrsEnd;
    let nextI;

    if (selfClose !== -1 && (gt === -1 || selfClose < gt)) {
      attrsEnd = selfClose;
      nextI = selfClose + 2;
    } else if (gt !== -1) {
      const close = inner.indexOf('</path>', gt);
      attrsEnd = gt;
      nextI = close === -1 ? inner.length : close + '</path>'.length;
    } else {
      break;
    }

    const attrs = inner.slice(pos, attrsEnd).trim();
    const d = matchAttr(attrs, 'd');
    if (d) {
      const fill = matchAttr(attrs, 'fill');
      const fillRule = matchAttr(attrs, 'fill-rule', 'fillRule');
      const clipRule = matchAttr(attrs, 'clip-rule', 'clipRule');
      paths.push({
        d,
        fill,
        fillRule:
          fillRule === 'evenodd' || fillRule === 'nonzero'
            ? fillRule
            : undefined,
        clipRule:
          clipRule === 'evenodd' || clipRule === 'nonzero'
            ? clipRule
            : undefined,
      });
    }
    i = nextI;
  }
  return paths;
}

function parseSvgFile(content) {
  const normalized = normalizeFills(content);
  const viewBox = parseViewBox(normalized);
  const inner = svgInnerWithoutDefs(normalized);
  const paths = extractPaths(inner);
  return { viewBox, paths };
}

function emitPathsTs(glyphs) {
  const names = Object.keys(glyphs).sort((a, b) => a.localeCompare(b));
  const lines = [];
  lines.push(
    `/** Auto-generated by \`npm run icons:build\` (sources: \`src/icons/svg/*.svg\`). */`
  );
  lines.push(`export type PfIconPathDef = {`);
  lines.push(`  d: string`);
  lines.push(`  fill?: string`);
  lines.push(`  fillRule?: 'evenodd' | 'nonzero'`);
  lines.push(`  clipRule?: 'evenodd' | 'nonzero'`);
  lines.push(`}`);
  lines.push(``);
  lines.push(`export type PfIconGlyph = {`);
  lines.push(`  viewBox: string`);
  lines.push(`  paths: readonly PfIconPathDef[]`);
  lines.push(`}`);
  lines.push(``);
  lines.push(`export const PF_ICON_NAMES = [`);
  for (const n of names) lines.push(`  '${n}',`);
  lines.push(`] as const`);
  lines.push(``);
  lines.push(`export type PfIconName = (typeof PF_ICON_NAMES)[number]`);
  lines.push(``);
  lines.push(
    `export const PF_ICON_GLYPHS: Record<PfIconName, PfIconGlyph> = {`
  );

  for (const name of names) {
    const { viewBox, paths } = glyphs[name];
    lines.push(`  ${name}: {`);
    lines.push(`    viewBox: '${viewBox}',`);
    lines.push(`    paths: [`);
    for (const p of paths) {
      const parts = [`      { d: ${JSON.stringify(p.d)}`];
      if (p.fill) parts[0] += `, fill: ${JSON.stringify(p.fill)}`;
      if (p.fillRule) parts[0] += `, fillRule: '${p.fillRule}'`;
      if (p.clipRule) parts[0] += `, clipRule: '${p.clipRule}'`;
      parts[0] += ` },`;
      lines.push(parts[0]);
    }
    lines.push(`    ],`);
    lines.push(`  },`);
  }
  lines.push(`}`);
  lines.push(``);
  return lines.join('\n') + '\n';
}

function main() {
  if (!fs.existsSync(svgDir)) {
    console.error('Directory not found:', svgDir);
    process.exit(1);
  }
  const files = fs.readdirSync(svgDir).filter((f) => f.endsWith('.svg'));
  if (files.length === 0) {
    console.error('No .svg files in', svgDir);
    process.exit(1);
  }

  const glyphs = {};
  const used = new Set();

  for (const file of files.sort()) {
    const base = path.basename(file, '.svg');
    const name = kebabBaseToCamelCase(base);
    if (!isValidIconName(name)) {
      console.error(
        `Invalid icon name from file "${file}" → "${name}" (expected [a-z][a-zA-Z0-9]*)`
      );
      process.exit(1);
    }
    if (used.has(name)) {
      console.error(`Duplicate icon name "${name}" (file: ${file})`);
      process.exit(1);
    }
    used.add(name);
    const full = path.join(svgDir, file);
    const svg = fs.readFileSync(full, 'utf8');
    const { viewBox, paths } = parseSvgFile(svg);
    if (paths.length === 0) {
      console.error(`No <path> in ${file}`);
      process.exit(1);
    }
    glyphs[name] = { viewBox, paths };
  }

  fs.writeFileSync(outPath, emitPathsTs(glyphs), 'utf8');
  console.log('Wrote', outPath, 'icons:', used.size);
}

main();
